export function useLocale() {
  const t = computed(() => messages[locale.value] || messages.ru)

  function setLocale(l) {
    const next = resolveLocale(l)
    locale.value = next
    localStorage.setItem('consilium-locale', next)
  }

  return { locale, t, setLocale }
}
